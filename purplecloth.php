    <h1>Prayer Shawl</h1>
    <h2 class="makewhite"> The practice of women wearing a veil or prayer shawl dates back before the time of Christ, to
        Judaism. Women would cover their heads as a sign of modesty, such that, married women
        would "reserve" their full beauty of a head unveiled only for their husbands. St. Paul refers to
        the practice of women keeping their heads covered during times of prayer <a href="https://www.biblegateway.com/passage/?search=%281Cor.+11%3A2-16%29&version=NRSVCE" target="_blank" class="hyperlink-style">(1Cor. 11:2-16)</a>. This
        practice was an outward sign of a women's desire for humility and her obediential submission
        to God and her spouse. In addition, there is the tradition of St. Veronica, who used her veil to
        cleanse the face of Christ during his Passion. The <a href="https://www.canonlaw.info/masterpage1917.htm" target="_blank" class="hyperlink-style">1917 Code of Cannon Law</a> reflected the
        practice of wearing a veil stating, "women, however, should be with head covered and
        modestly dressed, particularly when they approach the Lord's table" (Canon 1262.2). In the
        earlier Rite of the Mass (the current Extraordinary Form), women were required to wear a head
        covering. While it is no longer required by Church Law, it is a devout practice, particularly as an
        imitation of the Blessed Virgin Mary and a sign of modesty and humility.
