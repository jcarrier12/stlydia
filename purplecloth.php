    <h2 class="makewhite">The color purple had great significance during the lifetime of St. Lydia. Purple cloth was rare
and difficult to make. For this reason, it was typically worn by the wealthy and particularly by
royalty. However, it acquired even greater significance during the Passion of Our Lord, as he
was clothed in purple and crowned with thorns in mockery <a href="https://www.biblegateway.com/passage/?search=%28Mk.+15%3A17%29&version=NRSVCE" target="_blank" class="hyperlink-style">(Mk. 15:17)</a>. Therefore, members of
this consorority wear the color purple for two reasons: first, as an
expression of their baptismal dignity as daughters of the King of Kings, and secondly, as a
reminder of their call to be united in heart and mind to Christ Crucified.</h2>
    
