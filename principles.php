	<h1>The Three Principles</h1>
	
	<h2>The Members look to Saint Lydia as a guide to true femininity. Saint Pope John Paul II
speaks of the feminine genius. Women have a genius that men do not. God created male and
female with equal dignity, honor and value; each beloved by God with a glorious destiny.
Equality in this sense does not undermine the unique and distinct genius of each. To foster that
which is authentically and uniquely “woman,” the Society delineates the following three
principals.</h2>
<img class="left" src="beautifulphoto.jpg" style="margin-right: 30px;">
	<ol>
		
		<li>Beauty: The four attributes of God are One, True, Good and Beautiful. Things that
are beautiful inspire us to greatness, wonder and awe. Beauty launches us into the
divine. It has been said that the world will be saved by beauty. There is nothing
more beautiful than the love which is the Most Holy Trinity, a love that was revealed
in the person of Jesus Christ. The Members commit themselves to live a life of
beauty, beautifying this world by their life, words and actions.</li><br>
	
		
		<li>Modesty: Genuine feminine beauty is always framed in modesty. Modest beauty
leads to holiness, unity and confidence. Immodest beauty, on the other hand, leads
to lust, disintegration and loneliness. The Members are committed to be modest in
their speech, appearance and actions. (1 Tm 2:10).</li><br>

		<li>Maternity: All women are called to be mothers, some physically, all spiritually. Each
Member by her feminine genius commits to building up goodness, hope and love
through her God-given gift of maternal instinct.</li>

	</ol>
