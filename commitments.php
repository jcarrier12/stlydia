<h1>Commitments</h1>
<div>
	<h3>Personal Prayer Life</h3>
	<p>Each member will have a committed and concrete daily, weekly, monthly, and annual prayer schedule. While each member is free to discern the specific nature of their prayer life, it must be written and approved by the Society’s Chaplain.</p>
</div>

<div>
	<h3>Personal Apostolate</h3>
	<p>While the first priority is always the fulfillment of each member’s vocation, each member will be faithfully involved in a specific apostolate which is chosen by the member and approved by the Chaplain.</p>
</div>

<div>
	<h3>Sorority</h3>
	<p>Each member will be provided with the first names of all the members of the Society as well as their apostolic activity and will explicitly include each member and her service in prayer.  The members are also encouraged to support one another personally and gather regularly as seems appropriate and helpful.</p>
</div>

<div>
	<h3>Witness</h3>
	<p>Each member will wear the Saint Lydia Society Prayer shawl at Mass and at all other appropriate occasions and be eager to share with others about the Society. Every year, each member will invite at least three other women to join the Society.</p>
</div>

